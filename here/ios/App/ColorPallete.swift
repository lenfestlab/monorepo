import UIKit

extension UIColor {

  class func beige() -> UIColor{
    return UIColor.init(red: 252/255, green: 247/255, blue: 239/255, alpha: 1)
  }

  class func offYellow() -> UIColor{
    return UIColor.init(red: 241/255, green: 221/255, blue: 187/255, alpha: 1)
  }

  class func offBlue() -> UIColor{
    return UIColor.init(red: 192/255, green: 216/255, blue: 224/255, alpha: 1)
  }

  class func offRed() -> UIColor{
    return UIColor.init(red: 196/255, green: 73/255, blue: 0/255, alpha: 1)
  }
}
